package com.example.demo.pojo;

public class Note {
    private int ggh;
    private String dx;
    private String rq;
    private String bt;
    private String nr;

    public int getGgh() {
        return ggh;
    }

    public void setGgh(int ggh) {
        this.ggh = ggh;
    }

    public String getDx() {
        return dx;
    }

    public void setDx(String dx) {
        this.dx = dx;
    }

    public String getRq() {
        return rq;
    }

    public void setRq(String rq) {
        this.rq = rq;
    }

    public String getBt() {
        return bt;
    }

    public void setBt(String bt) {
        this.bt = bt;
    }

    public String getNr() {
        return nr;
    }

    public void setNr(String nr) {
        this.nr = nr;
    }

    
}
